/**
 * 한국민족문화대백과사전 키워드 검색
 * 정적 데이터에서 키워드 매칭
 */
export function searchEncykorea(keyword: string): {
  found: boolean;
  count: number;
  data: EncykoreaEntry[];
  source: string;
} {
  if (!keyword || keyword.trim().length === 0) {
    return {
      found: true,
      count: STATIC_ENCYKOREA_DATA.length,
      data: STATIC_ENCYKOREA_DATA,
      source: "한국민족문화대백과사전 (한국학중앙연구원)",
    };
  }

  const tokens = keyword
    .toLowerCase()
    .split(/[\s,?.!]+/)
    .filter((t) => t.length > 1);

  const scored = STATIC_ENCYKOREA_DATA.map((entry) => {
    const searchable = [
      entry.title,
      entry.category,
      entry.summary,
      entry.content,
      ...entry.relatedKeywords,
    ]
      .join(" ")
      .toLowerCase();

    const score = tokens.reduce((acc, token) => {
      return acc + (searchable.match(new RegExp(token, "g")) ?? []).length;
    }, 0);

    return { entry, score };
  });

  const results = scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .map((s) => s.entry);

  return {
    found: results.length > 0,
    count: results.length,
    data: results,
    source: "한국민족문화대백과사전 (한국학중앙연구원)",
  };
}
